package nd.esp.service.lifecycle.services.notify.models;

public class NotifyInstructionalobjectivesRelationModel {
	private String chapterId;
	private String lessonId;
	private String instructionalobjectiveId;
	
	public String getChapterId() {
		return chapterId;
	}
	public void setChapterId(String chapterId) {
		this.chapterId = chapterId;
	}
	public String getLessonId() {
		return lessonId;
	}
	public void setLessonId(String lessonId) {
		this.lessonId = lessonId;
	}
	public String getInstructionalobjectiveId() {
		return instructionalobjectiveId;
	}
	public void setInstructionalobjectiveId(String instructionalobjectiveId) {
		this.instructionalobjectiveId = instructionalobjectiveId;
	}
}
