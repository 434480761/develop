package nd.esp.service.lifecycle.vos;

public class ChapterLessons {
	private String chapterId;
	private String chapterTitle;
	private String metrialId;
	private String metrialTitle;
	private String lessonId;
	public String getChapterId() {
		return chapterId;
	}
	public void setChapterId(String chapterId) {
		this.chapterId = chapterId;
	}
	public String getChapterTitle() {
		return chapterTitle;
	}
	public void setChapterTitle(String chapterTitle) {
		this.chapterTitle = chapterTitle;
	}
	public String getMetrialId() {
		return metrialId;
	}
	public void setMetrialId(String metrialId) {
		this.metrialId = metrialId;
	}
	public String getMetrialTitle() {
		return metrialTitle;
	}
	public void setMetrialTitle(String metrialTitle) {
		this.metrialTitle = metrialTitle;
	}
	public String getLessonId() {
		return lessonId;
	}
	public void setLessonId(String lessonId) {
		this.lessonId = lessonId;
	}
	
}
